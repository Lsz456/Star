package org.example.entity;

public class Film {
    //所属id
    private Long f_id;
    //电影名称
    private String f_title;
    //电影类型
    private String f_type;
    //地区
    private String f_area;
    //评分
    private String f_score;
    //电影年份
    private String f_year;
    //电影主演
    private String f_star;
    //电影导演
    private String f_director;
    //最后更新时间
    private String f_update_time;
    //电影简介
    private String f_Introduction;
    //电影图片
    private String f_images;
    //播放地址
    private String f_url;

    public Long getF_id() {
        return f_id;
    }

    public void setF_id(Long f_id) {
        this.f_id = f_id;
    }

    public String getF_title() {
        return f_title;
    }

    public void setF_title(String f_title) {
        this.f_title = f_title;
    }

    public String getF_type() {
        return f_type;
    }

    public void setF_type(String f_type) {
        this.f_type = f_type;
    }

    public String getF_area() {
        return f_area;
    }

    public void setF_area(String f_area) {
        this.f_area = f_area;
    }

    public String getF_score() {
        return f_score;
    }

    public void setF_score(String f_score) {
        this.f_score = f_score;
    }

    public String getF_year() {
        return f_year;
    }

    public void setF_year(String f_year) {
        this.f_year = f_year;
    }

    public String getF_star() {
        return f_star;
    }

    public void setF_star(String f_star) {
        this.f_star = f_star;
    }

    public String getF_director() {
        return f_director;
    }

    public void setF_director(String f_director) {
        this.f_director = f_director;
    }

    public String getF_update_time() {
        return f_update_time;
    }

    public void setF_update_time(String f_update_time) {
        this.f_update_time = f_update_time;
    }

    public String getF_Introduction() {
        return f_Introduction;
    }

    public void setF_Introduction(String f_Introduction) {
        this.f_Introduction = f_Introduction;
    }

    public String getF_images() {
        return f_images;
    }

    public void setF_images(String f_images) {
        this.f_images = f_images;
    }

    public String getF_url() {
        return f_url;
    }

    public void setF_url(String f_url) {
        this.f_url = f_url;
    }
}
